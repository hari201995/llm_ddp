import os
import time
import torch.multiprocessing as mp
import torch
import torch.distributed as dist


def setup(rank, world_size, backend_type):
    """
    Docstring for setup

    :param rank: raank of the device
    :param world_size: World size for distributed computing
    """
    os.environ["MASTER_ADDR"] = "localhost"
    os.environ["MASTER_PORT"] = "10000"
    dist.init_process_group(backend_type, rank=rank, world_size=world_size)


def distributed_all_reduce(rank, world_size, nums, back_type):
    """
    Docstring for distributed_all_reduce

    :param rank: raank of the device
    :param world_size: World size for distributed computing
    """

    warmup_iter = 5
    run_iters = 10
    if back_type == "gloo":
        to_device = "cpu"
    else:
        to_device = f"cuda:{rank}"
        torch.cuda.set_device(rank)

    # setup the distributed setup
    setup(rank=rank, world_size=world_size, backend_type=back_type)

    # data
    data = torch.rand(nums, device=to_device)
    try:
        # warmup
        dist.barrier()
        for _ in range(warmup_iter):
            dist.all_reduce(data, async_op=False)
        if back_type == "nccl":
            torch.cuda.synchronize()

        # timed run
        start = time.time()
        dist.barrier()
        for _ in range(run_iters):
            dist.all_reduce(data, async_op=False)
        if back_type == "nccl":
            torch.cuda.synchronize()

        end = time.time()
        total = (end - start) / run_iters
        elapsed = total  # seconds/iter measured on THIS rank
        t = torch.tensor([elapsed], device=to_device, dtype=torch.float64)
        dist.all_reduce(t, op=dist.ReduceOp.MAX)
    finally:
        if dist.is_initialized():
            dist.destroy_process_group()

    if rank == 0:
        print(
            f"World Size = {world_size} | \
            Items Size = {nums} \
            Time Taken = {t.item()}"
        )


def main():
    torch.set_num_threads(1)
    world_size = [2, 4, 6]
    backend_type = ["gloo", "nccl"]
    num_elements_list = [1, 10, 100, 1000]  # MB

    for b_type in backend_type:
        for w in world_size:
            for num_elements in num_elements_list:
                # Spawn
                mp.spawn(  # type: ignore
                    fn=distributed_all_reduce,
                    args=(w, int(num_elements * 1024**2 / 4), b_type),
                    nprocs=w,
                    join=True,
                )


if __name__ == "__main__":
    main()
